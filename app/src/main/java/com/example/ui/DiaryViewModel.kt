    // --- Load and Decrypt Entries specific to Decoy or Real Vault ---
    fun loadEntries() {
        viewModelScope.launch {
            repository.getEntriesFlow(isDecoy = isFakeVaultActive).collect { rawEntries ->
                if (isUnlocked && masterPassword.isNotEmpty()) {
                    decryptAndExposeEntries(rawEntries)
                }
            }
        }
    }

    private fun decryptAndExposeEntries(rawEntries: List<DiaryEntry>) {
        val decrypted = rawEntries.map { entry ->
            DecryptedDiaryEntry(
                id = entry.id,
                title = CryptoHelper.decrypt(entry.encryptedTitle, entry.titleIv, masterPassword),
                content = CryptoHelper.decrypt(entry.encryptedContent, entry.contentIv, masterPassword),
                mood = CryptoHelper.decrypt(entry.encryptedMood, entry.moodIv, masterPassword),
                imagePath = entry.encryptedImagePath,
                timestamp = entry.timestamp,
                isSynced = entry.isSynced
            )
        }
        activeEntries.value = decrypted
    }

    fun getDecryptedBitmap(filePath: String): Bitmap? {
        if (masterPassword.isEmpty()) return null
        return repository.loadDecryptedImage(filePath, masterPassword)
    }

    // --- Diary Manipulation Methods ---
    fun selectEntryForEdit(entry: DecryptedDiaryEntry) {
        editingEntryId = entry.id
        formTitle = entry.title
        formContent = entry.content
        formMood = entry.mood
        formSelectedImageBytes = null
        formSelectedImageBitmap = entry.imagePath?.let { getDecryptedBitmap(it) }
    }

    fun clearForm() {
        editingEntryId = null
        formTitle = ""
        formContent = ""
        formMood = "Vui vẻ 😊"
        formSelectedImageBytes = null
        formSelectedImageBitmap = null
        aiMoodAnalysisResult = ""
    }

    fun saveDiaryEntry(context: Context, onComplete: () -> Unit) {
        if (formTitle.trim().isEmpty() || formContent.trim().isEmpty()) {
            return
        }

        viewModelScope.launch {
            val encryptionPassword = masterPassword
            val success = repository.saveEntry(
                id = editingEntryId ?: 0,
                title = formTitle.trim(),
                content = formContent.trim(),
                mood = formMood,
                imageBytes = formSelectedImageBytes,
                dbPassword = encryptionPassword,
                isDecoy = isFakeVaultActive,
                context = context
            )

            if (success) {
                addSyncLog("📝 Ghi nhớ hoàn tất: [${formTitle.trim()}] vào lúc ${getFormattedTime()}")
                clearForm()
                onComplete()
                
                // Automatic Google Drive Instant Real-time Synchronization Trigger!
                if (syncAutoEnabled) {
                    triggerGoogleDriveSync(context)
                }
            }
        }
    }

    fun deleteDiaryEntry(id: Int, context: Context) {
        viewModelScope.launch {
            repository.deleteEntryById(id, context)
            addSyncLog("🗑️ Đã xóa bản ghi nhật ký khóa ID: $id")
            
            if (syncAutoEnabled) {
                triggerGoogleDriveSync(context)
            }
        }
    }

    fun importImageFromUri(context: Context, uri: android.net.Uri) {
        viewModelScope.launch {
            try {
                val inputStream: java.io.InputStream? = context.contentResolver.openInputStream(uri)
                val bytes = inputStream?.readBytes()
                if (bytes != null) {
                    formSelectedImageBytes = bytes
                    formSelectedImageBitmap = BitmapFactory.decodeByteArray(bytes, 0, bytes.size)
                    addSyncLog("🖼️ Đã chọn ảnh thành công (${bytes.size / 1024} KB). Ảnh sẽ được mã hóa đầu cuối.")
                }
            } catch (e: Exception) {
                e.printStackTrace()
                addSyncLog("⚠️ Lỗi khi đọc hình ảnh: ${e.localizedMessage}")
            }
        }
    }

