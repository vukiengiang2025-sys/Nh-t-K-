    private fun putSecureString(key: String, value: String) {
        if (value.isEmpty()) {
            sharedPrefs.edit().remove(key).apply()
        } else {
            val encrypted = AndroidKeyStoreHelper.encryptWithKeyStore(value)
            sharedPrefs.edit().putString(key, encrypted).apply()
        }
    }

    private fun getSecureString(key: String, defaultValue: String = ""): String {
        val rawValue = sharedPrefs.getString(key, "") ?: ""
        if (rawValue.isEmpty()) return defaultValue
        val decrypted = AndroidKeyStoreHelper.decryptWithKeyStore(rawValue)
        return if (decrypted.isNotEmpty()) {
            decrypted
        } else {
            // Fallback for legacy plain text values (backward compatibility)
            rawValue
        }
    }

