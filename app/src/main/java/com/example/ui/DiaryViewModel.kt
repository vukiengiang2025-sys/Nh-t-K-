    fun registerPassword(password: String, fakePassword: String = ""): Boolean {
        if (password.length < 4) {
            authError = "Mật khẩu tối thiểu phải từ 4 kí tự"
            return false
        }
        val salt = "DIARY_SALT_88"
        val hash = (password + salt).hashCode().toString()
        
        // 1. Hardware Keystore Encryption for Hash Storage (via putSecureString)
        putSecureString("password_hash", hash)

        // Generate Master Key for primary vault
        val realKey = CryptoHelper.generateRandomKey()
        val wrappedReal = CryptoHelper.encrypt(realKey, password)
        putSecureString("wrapped_key_pwd", wrappedReal.cipherText)
        putSecureString("wrapped_key_pwd_iv", wrappedReal.iv)

        // 2. Optional Decoy password hashing with Keystore encryption
        if (fakePassword.isNotEmpty()) {
            if (fakePassword == password) {
                authError = "Mật khẩu giả lập không được trùng mật khẩu thật!"
                return false
            }
            val fakeHash = (fakePassword + salt).hashCode().toString()
            putSecureString("fake_password_hash", fakeHash)
            isDecoyPasswordSet = true

            // Generate Master Key for fake vault and wrap it
            val decoyKey = CryptoHelper.generateRandomKey()
            val wrappedDecoy = CryptoHelper.encrypt(decoyKey, fakePassword)
            putSecureString("wrapped_key_decoy", wrappedDecoy.cipherText)
            putSecureString("wrapped_key_decoy_iv", wrappedDecoy.iv)
        } else {
            // Clean up any legacy decoy data if user deleted decoy password
            sharedPrefs.edit()
                .remove("fake_password_hash")
                .remove("wrapped_key_decoy")
                .remove("wrapped_key_decoy_iv")
                .apply()
            isDecoyPasswordSet = false
        }

        // 3. Generate a secure unique Recovery Code
        val generatedKey = generateSecureRecoveryCode()
        putSecureString("recovery_key_enc", generatedKey)
        recoveryKeyGenerated = generatedKey
        rawRecoveryKeyShown = generatedKey

        // Wrap primary key with Recovery Code
        val wrappedRec = CryptoHelper.encrypt(realKey, generatedKey)
        putSecureString("wrapped_key_recovery", wrappedRec.cipherText)
        putSecureString("wrapped_key_recovery_iv", wrappedRec.iv)
        
        isPasswordSet = true
        masterPassword = realKey
        isUnlocked = true
        isFakeVaultActive = false
        authError = null

        // Cache encrypted password for secure hardware-backed biometric unlock
        try {
            putSecureString("bio_master_password_enc", password)
        } catch (e: Exception) {
            e.printStackTrace()
        }
        
        addSyncLog("🔑 Thiết lập bộ mã hóa bọc khóa (Key Wrapping) tối tân qua Android Keystore thành công.")
        loadEntries()
        return true
    }

    private fun generateSecureRecoveryCode(): String {
        val chars = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789"
        val random = java.security.SecureRandom()
        val part1 = (1..4).map { chars[random.nextInt(chars.length)] }.joinToString("")
        val part2 = (1..4).map { chars[random.nextInt(chars.length)] }.joinToString("")
        val part3 = (1..4).map { chars[random.nextInt(chars.length)] }.joinToString("")
        return "RECOVERY-$part1-$part2-$part3"
    }

    fun unlockApp(password: String): Boolean {
        val salt = "DIARY_SALT_88"
        val passwordHashOfInput = (password + salt).hashCode().toString()

        // Retrieve decrypted values from secure helper
        val decryptedStoredHash = getSecureString("password_hash")
        val decryptedStoredFakeHash = getSecureString("fake_password_hash")
        
        if (decryptedStoredHash == passwordHashOfInput) {
            val wrappedKey = getSecureString("wrapped_key_pwd")
            val wrappedIv = getSecureString("wrapped_key_pwd_iv")
            
            val decryptedMasterKey = if (wrappedKey.isNotEmpty() && wrappedIv.isNotEmpty()) {
                CryptoHelper.decrypt(wrappedKey, wrappedIv, password)
            } else {
                // Backward compatibility migration: use password directly as Master Key and wrap it securely!
                val wrapped = CryptoHelper.encrypt(password, password)
                putSecureString("wrapped_key_pwd", wrapped.cipherText)
                putSecureString("wrapped_key_pwd_iv", wrapped.iv)
                password
            }

            masterPassword = decryptedMasterKey
            isUnlocked = true
            isFakeVaultActive = false
            authError = null

            // Cache encrypted password for secure hardware-backed biometric unlock
            try {
                putSecureString("bio_master_password_enc", password)
            } catch (e: Exception) {
                e.printStackTrace()
            }
            
            addSyncLog("🔓 Mở khóa ví chính: Chìa khóa gốc đã được giải nén thành công.")
            loadEntries()
            return true
        } else if (decryptedStoredFakeHash.isNotEmpty() && decryptedStoredFakeHash == passwordHashOfInput) {
            val wrappedKey = getSecureString("wrapped_key_decoy")
            val wrappedIv = getSecureString("wrapped_key_decoy_iv")
            
            val decryptedMasterKey = if (wrappedKey.isNotEmpty() && wrappedIv.isNotEmpty()) {
                CryptoHelper.decrypt(wrappedKey, wrappedIv, password)
            } else {
                val wrapped = CryptoHelper.encrypt(password, password)
                putSecureString("wrapped_key_decoy", wrapped.cipherText)
                putSecureString("wrapped_key_decoy_iv", wrapped.iv)
                password
            }

            masterPassword = decryptedMasterKey
            isUnlocked = true
            isFakeVaultActive = true
            authError = null
            
            addSyncLog("🎭 Mở không gian ngụy trang (Fake Vault). Dữ liệu bảo vệ hoạt động.")
            loadEntries()
            return true
        } else {
            // Check if input is the Recovery Code
            val decryptedRecovery = getSecureString("recovery_key_enc")
            if (decryptedRecovery.isNotEmpty() && decryptedRecovery.equals(password.trim(), ignoreCase = true)) {
                // Verify and extract Master Key instead of logging in directly (Security Reviewer protection)
                val wrappedKey = getSecureString("wrapped_key_recovery")
                val wrappedIv = getSecureString("wrapped_key_recovery_iv")
                
                val decryptedMasterKey = if (wrappedKey.isNotEmpty() && wrappedIv.isNotEmpty()) {
                    CryptoHelper.decrypt(wrappedKey, wrappedIv, password.trim())
                } else {
                    // Legacy recovery bypass: masterPassword before was "RECOVERY_SESSION_BYPASS" decrypting with "RECOVERY_KEY_DECRYPT"
                    "RECOVERY_KEY_DECRYPT"
                }

                if (decryptedMasterKey.isEmpty() || decryptedMasterKey.startsWith("⚠️")) {
                    authError = "⚠️ Mã cứu hộ không chính xác hoặc dữ liệu bọc khóa đã bị hỏng!"
                    return false
                }

                tempDecryptedMasterKey = decryptedMasterKey
                isRecoveryAuthSuccess = true
                authError = null
                addSyncLog("🔑 Mã cứu hộ chính xác. Tiến trình khôi phục sẵn sàng. Yêu cầu đổi mật khẩu mới.")
                return true
            }
            
            authError = "⚠️ Mật khẩu hoặc mã cứu hộ không chính xác!"
            return false
        }
    }

    fun resetPasswordViaRecoverySession(newPassword: String): Boolean {
        val activeKey = tempDecryptedMasterKey
        if (activeKey != null) {
            val salt = "DIARY_SALT_88"
            val hash = (newPassword + salt).hashCode().toString()
            
            // Wrap the activeKey with the new password
            val wrappedReal = CryptoHelper.encrypt(activeKey, newPassword)
            
            putSecureString("password_hash", hash)
            putSecureString("wrapped_key_pwd", wrappedReal.cipherText)
            putSecureString("wrapped_key_pwd_iv", wrappedReal.iv)
            putSecureString("bio_master_password_enc", newPassword)

            // Also re-wrap the activeKey with the current recovery key so it's fresh and reusable!
            val decryptedRecovery = getSecureString("recovery_key_enc")
            if (decryptedRecovery.isNotEmpty()) {
                val wrappedRec = CryptoHelper.encrypt(activeKey, decryptedRecovery)
                putSecureString("wrapped_key_recovery", wrappedRec.cipherText)
                putSecureString("wrapped_key_recovery_iv", wrappedRec.iv)
            }
            
            masterPassword = activeKey
            isUnlocked = true
            isFakeVaultActive = false
            tempDecryptedMasterKey = null
            isRecoveryAuthSuccess = false
            authError = null
            
            addSyncLog("🔄 Đặt lại và bọc lại khóa chính tự động thành công. Bạn đã được khôi phục.")
            loadEntries()
            return true
        }
        return false
    }

